import Foundation

/*  Design an algorithm that converts degrees Celsius to Fahrenheit. Note: The conversion formula is F = C x 9/5 + 32. */

let celsius = 100.0

let fahrenheit = celsius * 9 / 5 + 32

print("The temperature in degrees Fahrenheit is: \(String(format: "%.2f", fahrenheit))")

print("\(celsius) ºC = \(fahrenheit) ºF")

